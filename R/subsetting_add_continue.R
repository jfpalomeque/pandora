subsetting_add_continue<-function(data){
  variable<-select.list(colnames(data))
  valor<-select.list(levels(unlist(data[variable])))
  marks_selected<-subset(data, data[variable]==valor)
  marks_subsetted$LM1x<-as.numeric(as.character(marks_subsetted$LM1x))
  marks_subsetted$LM1y<-as.numeric(as.character(marks_subsetted$LM1y))
  marks_subsetted$LM2x<-as.numeric(as.character(marks_subsetted$LM2x))
  marks_subsetted$LM2y<-as.numeric(as.character(marks_subsetted$LM2y))
  marks_subsetted$LM3x<-as.numeric(as.character(marks_subsetted$LM3x))
  marks_subsetted$LM3y<-as.numeric(as.character(marks_subsetted$LM3y))
  marks_subsetted$LM4x<-as.numeric(as.character(marks_subsetted$LM4x))
  marks_subsetted$LM4y<-as.numeric(as.character(marks_subsetted$LM4y))
  marks_subsetted$LM5x<-as.numeric(as.character(marks_subsetted$LM5x))
  marks_subsetted$LM5y<-as.numeric(as.character(marks_subsetted$LM5y))
  marks_subsetted$LM6x<-as.numeric(as.character(marks_subsetted$LM6x))
  marks_subsetted$LM6y<-as.numeric(as.character(marks_subsetted$LM6y))
  marks_subsetted$LM7x<-as.numeric(as.character(marks_subsetted$LM7x))
  marks_subsetted$LM7y<-as.numeric(as.character(marks_subsetted$LM7y))
  marks_subsetted$WIS<-as.numeric(as.character(marks_subsetted$WIS))
  marks_subsetted$WIM<-as.numeric(as.character(marks_subsetted$WIM))
  marks_subsetted$WIB<-as.numeric(as.character(marks_subsetted$WIB))
  marks_subsetted$LDC<-as.numeric(as.character(marks_subsetted$LDC))
  marks_subsetted$RDC<-as.numeric(as.character(marks_subsetted$RDC))
  marks_subsetted$SI<-as.numeric(as.character(marks_subsetted$SI))
  marks_subsetted$d<-as.numeric(as.character(marks_subsetted$d))
  marks_subsetted$OA<-as.numeric(as.character(marks_subsetted$OA))
  marks_subsetted<-rbind(marks_subsetted, marks_selected)
  marks_subsetted<-as.matrix(marks_subsetted)
  marks_subsetted<<-as.data.frame(marks_subsetted)
  stop(subsetting_DB_menu_bis())
}
